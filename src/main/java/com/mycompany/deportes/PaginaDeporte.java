    public int eliminar(int position){
        for(int i=position;i<=countTeams-position;i++)
	{
            if(i+1>countTeams-1)
		return 1;
            else
		teamsList[i] = teamsList[i+1];
	}
	return 1;
    }
    
    public int searchTeam(){
        boolean flag=false;
        int result=0;
        if(!idText.getText().isEmpty()){
            int id=Integer.parseInt(idText.getText());
            if(countTeams>0){
                for(int i=0;i<countTeams;i++){
                    if(teamsList[i].getId()==id){
                        flag=true;
                        nameText.setText(teamsList[i].getNameTeam());
                        victoriesText.setText(String.valueOf(teamsList[i].getVictories()));
                        cityText.setText(teamsList[i].getCity());
                        message.setText("Equipo Eliminado.");
                        result=i;
                        System.out.println(i);
                        return result;
                    }
                }if(flag==false){
                    message.setText("Equipo no encontrado.");
                    return -1;
                }
            }else{
                message.setText("No hay registros.");
            }
            
        }
        return result;
    }
