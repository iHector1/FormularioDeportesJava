    public void addTeam(){
        if(idText.getText().isEmpty()||nameText.getText().isEmpty()||
                victoriesText.getText().isEmpty()||cityText.getText().isEmpty()){
            message.setText("Los capos estan vacios");
        }else if(countTeams<tsize){
            
            teamsList[countTeams].setNameTeam(nameText.getText());
            teamsList[countTeams].setCity(cityText.getText());
            int id=Integer.parseInt(idText.getText());
            int victories=Integer.parseInt(victoriesText.getText());
            teamsList[countTeams].setId(id);
            teamsList[countTeams].setVictories(victories);            
            countTeams++;
            clear();
            message.setText("Equipo Registrado");  
            
        }else{
            message.setText("Lista llena");            
        }
    }
    
    
    
